    public void loadAlunos(){
        FileReader f = null;
                try {
            this.lista = new ArrayList<>();
            f = new FileReader("ListagemAlunos.csv");
            Scanner arquivoLido = new Scanner(f);
            arquivoLido.useDelimiter("\n");

            String linhaLida = "";
            if (arquivoLido.hasNext()) //para evitar de ler arquivo vazio
            {
                linhaLida = arquivoLido.next();
            }

            while (arquivoLido.hasNext()) {
                linhaLida = arquivoLido.next();

                Aluno a = new Aluno();
                a.CSVToAtributo(linhaLida);
                this.lista.add(a);
            }
            //Atualiza a listagem
            edtListagemAlunos.setText(imprimirListaAlunos());
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                f.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }
    
        public String imprimirListaAlunos() {
        String listagemCompleta = "";

        for (int i = 0; i <= lista.size() - 1; i++) {
            Aluno aluno = lista.get(i);

            listagemCompleta = listagemCompleta + aluno.toString();
        }
        return listagemCompleta;
    }
    
    

