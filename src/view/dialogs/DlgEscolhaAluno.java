    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String matriculaEscolhida = edtMatricula.getText();        
        if (!matriculaEscolhida.isEmpty()) {
            String id = matriculaEscolhida;
            alunoEscolhido = this.pesquisaAluno(id);

            if (alunoEscolhido == null) {
                JOptionPane.showMessageDialog(this, "Não existe este CPF.");
                lblAlunoEscolhido.setText("");
            } else {
                lblAlunoEscolhido.setText(alunoEscolhido.toString());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAdicionarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarAlunoActionPerformed
            if (alunoEscolhido == null) {
                int response = JOptionPane.showConfirmDialog(null,
                        "Nenhum professor foi escolhido. Deseja continuar?",
                        "Confirm",
                        JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (response == JOptionPane.OK_OPTION) {
                    dispose();
            }
        } else {
            this.disciplina.getLstAlunos().add(alunoEscolhido);
            
            dispose();
        }
    }//GEN-LAST:event_btnAdicionarAlunoActionPerformed

    public Aluno pesquisaAluno(String id) {
        for (int i = 0; i <= lista.size() - 1; i++) {
            Aluno aluno = lista.get(i);

            if (aluno.getMatricula().equals(id)) {
                return aluno;
            }
        }
        return null;
    }
    
