    public void loadProfessores(){
        FileReader f = null;
                try {
            this.lista = new ArrayList<>();
            f = new FileReader("ListagemProfessores.csv");
            Scanner arquivoLido = new Scanner(f);
            arquivoLido.useDelimiter("\n");

            String linhaLida = "";
            if (arquivoLido.hasNext()) //para evitar de ler arquivo vazio
            {
                linhaLida = arquivoLido.next();
            }

            while (arquivoLido.hasNext()) {
                linhaLida = arquivoLido.next();

                Professor p = new Professor();
                p.CSVToAtributo(linhaLida);
                this.lista.add(p);
            }
            //Atualiza a listagem
            edtListagemProfessores.setText(imprimirListaProfessores());
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                f.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }
    
        public String imprimirListaProfessores() {
        String listagemCompleta = "";

        for (int i = 0; i <= lista.size() - 1; i++) {
            Professor professor = lista.get(i);

            listagemCompleta = listagemCompleta + professor.toString();
        }
        return listagemCompleta;
    }
    
    

