    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String cpfEscolhido = edtCpf.getText();        
        if (!cpfEscolhido.isEmpty()) {
            int id = Integer.parseInt(cpfEscolhido);
            professorEscolhido = this.pesquisaProfessor(id);

            if (professorEscolhido == null) {
                JOptionPane.showMessageDialog(this, "Não existe este CPF.");
                lblProfessorEscolhido.setText("");
            } else {
                lblProfessorEscolhido.setText(professorEscolhido.toString());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAdicionarProfessorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProfessorActionPerformed
            if (professorEscolhido == null) {
                int response = JOptionPane.showConfirmDialog(null,
                        "Nenhum professor foi escolhido. Deseja continuar?",
                        "Confirm",
                        JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (response == JOptionPane.OK_OPTION) {
                    dispose();
            }
        } else {
            this.disciplina.setMinistrante(professorEscolhido);
            
            dispose();
        }
    }//GEN-LAST:event_btnAdicionarProfessorActionPerformed

    public Professor pesquisaProfessor(int id) {
        for (int i = 0; i <= lista.size() - 1; i++) {
            Professor prof = lista.get(i);

            if (Integer.parseInt(prof.getCpf()) == id) {
                return prof;
            }
        }
        return null;
    }
    
