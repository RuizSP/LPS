    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        this.limparCampos();
        edtNome.setEnabled(false);
        edtSemestre.setEnabled(false);
        edtHorario.setEnabled(false);
        btnAdicionarAluno.setEnabled(false);
        btnAdicionarProfessor.setEnabled(false);
    }//GEN-LAST:event_btnCancelarActionPerformed

    public void limparCampos() {
        edtNome.setText("");
        edtSemestre.setText("");
        edtHorario.setText("");
        edtDisciplina.setText("");
    }
    private void btnCancelarMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCancelarMouseExited
        btnCancelar.setBackground(null);
    }//GEN-LAST:event_btnCancelarMouseExited

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(!edtNome.getText().isEmpty()){
            this.disciplina.setNome(edtNome.getText()); 
        }
        if(!edtSemestre.getText().isEmpty()){
            this.disciplina.setSemestre(Integer.parseInt(edtSemestre.getText()));
        }
        if(!edtHorario.getText().isEmpty()){
            this.disciplina.setHorario(edtHorario.getText()); 
        }
        atualizaInterfaceDisciplina();
        lstDisciplinas.add(disciplina);
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

